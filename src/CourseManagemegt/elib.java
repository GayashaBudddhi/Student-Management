    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked

        JFileChooser chooser= new JFileChooser();
        chooser.showOpenDialog(null);
        File f=chooser.getSelectedFile();
        String filename=f.getAbsolutePath();
        
        String CourseName=cName.getText();
        String LecName=cName.getText();
        String Title=title.getText();
        String Date=((JTextField)dateC.getDateEditor().getUiComponent()).getText();
       
        path.setText(filename);
        attach(CourseName,LecName,Title,Date,filename);
        
        
       
    }//GEN-LAST:event_jButton1MouseClicked
    
    public void attach(String Cnamee, String Lname,String title, String date,String file){
        
                
         try {
             
            FileInputStream fis=new FileInputStream(file);
            Class.forName("org.apache.derby.jdbc.ClientDriver");
            Connection conn=DriverManager.getConnection("jdbc:derby://localhost:1527/studentManagement","root","root");
            
            PreparedStatement pstmt = conn
            .prepareStatement("insert into lib(coursename,lecnamename,title,date,file) values (?,?,?,?,?)");
            
            pstmt.setString(1,Cnamee);
            pstmt.setString(2, Lname);
            pstmt.setString(3, title);
            pstmt.setString(4, date);
            
            
            pstmt.setBinaryStream(5, fis);
            
            pstmt.executeUpdate();
            
            
            
        


// TODO add your handling code here:
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(elib.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(elib.class.getName()).log(Level.SEVERE, null, ex);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(elib.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
    }
    
