    public void attach(String lecName, String lecNo,String file){
        
                
         try {
            FileInputStream fis=new FileInputStream(file);
            Class.forName("org.apache.derby.jdbc.ClientDriver");
            Connection conn=DriverManager.getConnection("jdbc:derby://localhost:1527/studentManagement","root","root");
            
            PreparedStatement pstmt = conn.prepareStatement("insert into lib(lecName,lecNo,file) values (?,?,?)");
            
            pstmt.setString(1,lecName);
            pstmt.setString(2, lecNo);
            pstmt.setBinaryStream(3, fis);
            
            pstmt.executeUpdate();
            
            
            
        


// TODO add your handling code here:
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(LectureUpload.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(LectureUpload.class.getName()).log(Level.SEVERE, null, ex);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(LectureUpload.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
    }
    
    
    
    
    
    
    
