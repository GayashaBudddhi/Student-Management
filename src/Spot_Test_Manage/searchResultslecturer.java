    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        String pass = pp.getText();
        String w = week.getText();
        try {
                                    Class.forName("org.apache.derby.jdbc.ClientDriver");
                                    Connection con=DriverManager.getConnection("jdbc:derby://localhost:1527/studentManagement","root","root");

                                   String namesql="select * from spotresult where pp=? AND week=?";
                                    
                                    PreparedStatement pst=con.prepareStatement(namesql);
                                    pst.setString(1, pass);
                                    pst.setString(2, w);
                                    
                                    ResultSet rs=pst.executeQuery();
                                    while(rs.next()){
                                    String writ = rs.getString(3);
                                    String read = rs.getString(4);
                                    String list = rs.getString(5);
                                    String spe = rs.getString(6);
                                   
                                  
                                      writing.setText(writ);
                                      reading.setText(read);
                                      listening.setText(list);
                                      speaking.setText(spe);
                                      
                                      
                                 
                                   }
                                  

                                } catch (ClassNotFoundException ex) {
                                    Logger.getLogger(login.class.getName()).log(Level.SEVERE, null, ex);
                                } catch (SQLException ex) {
                                    Logger.getLogger(login.class.getName()).log(Level.SEVERE, null, ex);
                                }
        
        
        
    }//GEN-LAST:event_jButton1MouseClicked

